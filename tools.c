void	calc_stop(int x, int y, int **map, int *stop)
{
	if (y > 0 && *stop == 0 && map[y - 1][x] == -2000000000)
		*stop = 1;
}

int		ft_abs(int a, int b)
{
	int	c;

	c = a - b;
	if (c < 0)
		c *= -1;
	return (c);
}
